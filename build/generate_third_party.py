"""Generate bundled third-party notices for the installer (§2.3, §12.2)."""

from __future__ import annotations

from dataclasses import dataclass
from importlib import metadata
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parent.parent
OUTPUT_FILE = REPO_ROOT / "resources" / "licenses" / "THIRD-PARTY.md"


@dataclass(frozen=True)
class PackageSpec:
    distribution: str
    display_name: str
    role: str
    homepage: str | None = None


@dataclass(frozen=True)
class NoticeEntry:
    name: str
    role: str
    version: str
    license_name: str
    homepage: str
    summary: str


PYTHON_COMPONENTS = (
    PackageSpec(
        distribution="stormfuse",
        display_name="StormFuse",
        role="Application package",
        homepage="https://github.com/winds-of-storm/stormfuse",
    ),
    PackageSpec(
        distribution="PyQt6",
        display_name="PyQt6",
        role="Python bindings for Qt",
    ),
    PackageSpec(
        distribution="PyQt6-Qt6",
        display_name="PyQt6-Qt6",
        role="Bundled Qt 6 runtime subset",
    ),
    PackageSpec(
        distribution="PyQt6-sip",
        display_name="PyQt6-sip",
        role="PyQt6 support module",
    ),
)

MANUAL_COMPONENTS = (
    NoticeEntry(
        name="Python 3.12 runtime",
        role="Bundled application runtime",
        version="3.12+",
        license_name="PSF-2.0",
        homepage="https://www.python.org/",
        summary="Bundled by PyInstaller for the packaged desktop application.",
    ),
    NoticeEntry(
        name="FFmpeg (gyan.dev essentials build)",
        role="Bundled executable tools",
        version="See `FFMPEG-SOURCE.txt`",
        license_name="GPL-2.0-or-later",
        homepage="https://www.gyan.dev/ffmpeg/builds/",
        summary=(
            "Ships as `ffmpeg.exe` and `ffprobe.exe`; corresponding source "
            "details and pinned hashes live in `FFMPEG-SOURCE.txt`."
        ),
    ),
)


def _first_nonempty(*values: str | None) -> str | None:
    for value in values:
        if value:
            stripped = value.strip()
            if stripped:
                return stripped
    return None


def _project_urls(dist_metadata: metadata.PackageMetadata) -> dict[str, str]:
    urls: dict[str, str] = {}
    for item in dist_metadata.get_all("Project-URL", []):
        label, _, url = item.partition(",")
        if url:
            urls[label.strip().lower()] = url.strip()
    return urls


def _homepage_for(spec: PackageSpec, dist_metadata: metadata.PackageMetadata) -> str:
    project_urls = _project_urls(dist_metadata)
    preferred_url = _first_nonempty(
        spec.homepage,
        project_urls.get("homepage"),
        project_urls.get("source"),
        project_urls.get("documentation"),
        dist_metadata.get("Home-page"),
    )
    if preferred_url is None:
        raise RuntimeError(f"Could not determine homepage for {spec.distribution}")
    return preferred_url


def _license_for(dist_metadata: metadata.PackageMetadata) -> str:
    license_name = _first_nonempty(
        dist_metadata.get("License-Expression"),
        dist_metadata.get("License"),
    )
    if license_name is None:
        raise RuntimeError("Could not determine package license from metadata")
    return license_name


def load_package_notice(spec: PackageSpec) -> NoticeEntry:
    dist = metadata.distribution(spec.distribution)
    dist_metadata = dist.metadata
    summary = _first_nonempty(dist_metadata.get("Summary"), "No summary provided.") or ""
    return NoticeEntry(
        name=spec.display_name,
        role=spec.role,
        version=dist.version,
        license_name=_license_for(dist_metadata),
        homepage=_homepage_for(spec, dist_metadata),
        summary=summary,
    )


def build_notice_entries() -> list[NoticeEntry]:
    package_entries = [load_package_notice(spec) for spec in PYTHON_COMPONENTS]
    return [*package_entries, *MANUAL_COMPONENTS]


def render_markdown(entries: list[NoticeEntry]) -> str:
    lines = [
        "# Third-Party Notices",
        "",
        "Generated by `build/generate_third_party.py` from installed package metadata where available.",
        "This file covers bundled components that ship in the StormFuse installer.",
        (
            "Build-only and dev-only tools such as PyInstaller, NSIS, pytest, ruff, mypy, "
            "and pylint are intentionally omitted because they are not distributed with the app."
        ),
        "",
        "## Bundled Components",
        "",
    ]
    for entry in entries:
        lines.extend(
            (
                f"### {entry.name}",
                f"- Role: {entry.role}",
                f"- Version: {entry.version}",
                f"- License: {entry.license_name}",
                f"- Homepage: {entry.homepage}",
                f"- Summary: {entry.summary}",
                "",
            )
        )
    lines.extend(
        (
            "## Bundled License Files",
            "",
            "- `GPL-3.0.txt` — StormFuse",
            "- `GPL-2.0.txt` — FFmpeg",
            "- `FFMPEG-SOURCE.txt` — FFmpeg build provenance and source pointers",
            "",
        )
    )
    return "\n".join(lines)


def write_notice_file(output_file: Path = OUTPUT_FILE) -> Path:
    output_file.parent.mkdir(parents=True, exist_ok=True)
    output_file.write_text(render_markdown(build_notice_entries()), encoding="utf-8")
    return output_file


def main() -> None:
    written = write_notice_file()
    print(f"Wrote {written}")


if __name__ == "__main__":
    main()
